        /**
         * 判断身份证是否合法
         *
         * @param string $idCard 身份证号码
         * @return bool 是否合法
         */
        public function is(string $idCard): bool
        {
            // 基本格式校验
            if (!preg_match('/^[\d]{17}[\dX]$/i', $idCard)) {
                return false;
            }

            // 提取关键信息
            $provinceCode = substr($idCard, 0, 2);
            $birthDate = substr($idCard, 6, 8);
            $sequenceCode = substr($idCard, 14, 3);
            $checkCode = strtoupper(substr($idCard, 17, 1));

            // 校验省份代码（11-82为有效行政区划代码）
            if ($provinceCode < '11' || $provinceCode > '82') {
                return false;
            }

            // 校验出生日期
            $year = substr($birthDate, 0, 4);
            $month = substr($birthDate, 4, 2);
            $day = substr($birthDate, 6, 2);
            if (!checkdate((int)$month, (int)$day, (int)$year)) {
                return false;
            }

            // 校验顺序码
            if (!ctype_digit($sequenceCode)) {
                return false; // 顺序码必须全为数字
            }

            // 校验码验证
            $factor = [7, 9, 10, 5, 8, 4, 2, 1, 6, 3, 7, 9, 10, 5, 8, 4, 2];
            $map = ['1', '0', 'X', '9', '8', '7', '6', '5', '4', '3', '2'];

            $sum = 0;
            for ($i = 0; $i < 17; $i++) {
                $sum += (int)$idCard[$i] * $factor[$i];
            }

            return $map[$sum % 11] === $checkCode;
        }

        /**
         * 获取性别（1 男，2 女）
         *
         * @param string $idCard 身份证号码
         * @return int 性别（1 表示男性，2 表示女性）
         */
        public function sex(string $idCard): int
        {
            if ( !$this->is($idCard) ) {
                return '';
            }

            // 获取第17位数字
            $genderDigit = (int)substr($idCard, 16, 1);

            // 判断性别
            return ($genderDigit % 2 === 0) ? 2 : 1;
        }

        /**
         * 获取生日
         *
         * @param string $idCard 身份证号码
         * @return string 生日（格式：YYYY-MM-DD）
         */
        public function birthday(string $idCard): string
        {
            if ( !$this->is($idCard) ) {
                return '';
            }

            // 提取出生日期部分（第7位到第14位）
            $birthdayPart = substr($idCard, 6, 8);

            // 格式化输出
            $year = substr($birthdayPart, 0, 4);
            $month = substr($birthdayPart, 4, 2);
            $day = substr($birthdayPart, 6, 2);

            return "$year-$month-$day";
        }

        /**
         * 获取年龄
         * @param string $idCard
         * @return int
         */
        public function age(string $idCard): int
        {
            $birthday = $this->birthday($idCard);

            // 创建出生日期对象
            $birthDateTime = DateTime::createFromFormat('Y-m-d', $birthday);

            // 创建当前日期对象
            $currentDateTime = new DateTime();

            // 计算日期差
            return $currentDateTime->diff($birthDateTime)->y;
        }

        /**
         * 获取省份
         *
         * @param string $idCard :身份证号码或身份证号码的前2位
         * @return string 省份名称
         */
        public function province( string $idcard ): string
        {
            $provinceCode = $this->province_code($idcard) ?: $idcard;

            // 返回省份名称，默认返回空字符串
            return $this->provinceMap[$provinceCode] ?? '';
        }

        /**
         * 获取省份代码
         * @param string $idCard
         * @return string
         */
        public function province_code(string $idCard): string
        {
            return $this->is($idCard) ? substr($idCard, 0, 2) : '';
        }

        /**
         * 获取城市
         *
         * @param string $idCard :身份证号码或身份证号码的前4位
         * @return string 城市名称
         */
        public function city(string $idCard): string
        {
            $cityCode = $this->city_code($idCard) ?: $idCard;

            // 返回城市名称，默认返回空字符串
            return $this->cityMap[$cityCode] ?? '';
        }

        /**
         * 获取城市代码
         * @param string $idCard
         * @return string
         */
        public function city_code(string $idCard): string
        {
            return $this->is($idCard) ? substr($idCard, 0, 4) : '';
        }


    }
